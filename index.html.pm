◊headline{About}

◊portrait["assets/myface.jpg" "a picture of my face"] I am an MS student at UC Davis studying computer science.

Previously, I was a Technical Consultant at Hitachi Vantara, working on cloud storage at eBay. Even before that, I graduated from the University of Chicago (BS '21) with a degree in computer science and a minor in English and creative writing.

I'm interested in ◊strong{programming languages} and ◊strong{formal methods research} to ◊strong{improve the quality of software systems} and ◊strong{make it easier to develop higher quality software}. Software should be more empowering and less frustrating for both developers and users.

More broadly, I'm interested in moving software development away from heuristics and hacks and onto more solid, elegant, and principled ground. I think there is a lot of room for mathematical abstractions to give us much better results across the computing stack.

