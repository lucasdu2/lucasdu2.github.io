◊headline{Research Interests}
I'm primarily interested in research at the intersection of ◊strong{programming languages}, ◊strong{formal methods}, and ◊strong{systems}. It should be easier to create software --- particularly concurrent and distributed software --- that is more correct, efficient, secure, private, and flexible. Programming languages and formal methods offer a theoretically rich and principled place to start.

Currently, I'm interested in the formal verification of concurrent and distributed systems. I want to prove things about programs. I'm also interested in better programming models for parallel and distributed computing.

